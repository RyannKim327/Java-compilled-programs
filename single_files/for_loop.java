public class for_loop{
	public static void main(String[] args){
		// format
		// for(intialization; condition; increment)
		// in increment/decrement there are also called prefix and postfix indcrementation/decrementation
		// prefix i++ or i--
		// postfix ++i or --i
		for(int i = 0; i < 10; i++){
			System.out.println("Number " + i);
		}
	}
}